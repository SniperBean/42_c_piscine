void	ft_putchar(char c);

void	rush(int h, int v)
{
	int i;
	int j;

	j = 0;
	while (i <= v && v >= 0 && h >= 0)
	{
		i = 0;
		while (i < h)
		{
			if ((i == 0 && j == 0) || (i == 0 && j == v - 1) ||
				(i == h - 1 && j == 0) || (i == h - 1 && j == v - 1))
				ft_putchar('o');
			else if (i == 0 || i == h - 1)
				ft_putchar('|');
			else if (j == 0 || j == v - 1)
				ft_putchar('-');
			else
				ft_putchar(' ');
			i++;
		}
		ft_putchar('\n');
		j++;
	}
}
