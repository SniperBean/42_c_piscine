void	ft_putchar(char c);

void	rush(int h, int v)
{
	int i;
	int j;

	i = 0;
	h--;
	v--;
	while (i <= v && v >= 0  && h >= 0)
	{
		j = 0;
		while (j <= h)
		{
			if ((i == 0 && j == 0) || (i == v && j == 0))
				ft_putchar('A');
			else if (((i == 0 || i == v) && (j > 0 && j < h)) ||
					((j == 0 || j == h) && (i > 0 && i < v)))
				ft_putchar('B');
			else if ((j != 0 && j == h && (i == v || i == 0)))
				ft_putchar('C');
			else
				ft_putchar(' ');
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
