int	ft_is_prime(int nb)
{
	int count;

	count = 2;
	if (nb <= 1)
		return (0);
	while (nb >= 2 && nb % count != 0)
	{
		count++;
	}
	if (count == nb)
		return (1);
	else
		return (0);
}
