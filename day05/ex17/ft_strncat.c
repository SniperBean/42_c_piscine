char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	nb = nb / 4;
	while (dest[i] != '\0' || dest[i] != 0)
	{
		i++;
	}
	while ((src[j] != '\0' || src[j] != 0) && i < nb)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
