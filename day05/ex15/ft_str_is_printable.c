int	ft_str_is_printable(char *str)
{
	int i;
	int flag;

	i = 0;
	flag = 0;
	while (str[i] != '\0' || str[i] != 0)
	{
		flag = 0;
		if (str[i] >= '!' && str[i] <= '~')
			flag = 1;
		else
			return (0);
		i++;
	}
	if (i == 0)
		return (1);
	return (flag);
}
